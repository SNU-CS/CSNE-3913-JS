// Printing to the console
//
// Probably the most important lesson!
console.log("Hello, world!");

// Basic for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Basic while loop
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// Basic iteration
const arr = [1, 2, 3];
for (const i of arr) {
    console.log(i);
}

// Breaking out of a loop
while (true) {
    console.log(i);
    if (i === 10) {
        break;
    }
    i++;
}
